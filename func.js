function sumOddNumbers(limit){
    let sum = 0;
    for(let i = 1; i <= limit; i++){
        if(i % 2 !== 0){
            sum += i;
        }
    }
    console.log("Sum of odd numbers from 1 to", limit, "is:", sum);
}

sumOddNumbers(20);






